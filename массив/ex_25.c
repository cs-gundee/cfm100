#include <stdio.h>
 
int main() {
   int n[ 10 ]; /* 10ш бүхэл тоо хадгалж чадах хувьсагч*/
   int i; /* тоолуур хувьсагч */
   
   /* давталтаар массивын элемент бүрд хандах*/
   for ( i = 0; i < 10; i++ ) {
      n[ i ] = 0; /* i байрлал дахь элементийг 0 болгож тохируулах */
   } 

   printf( "%s%13s\n", "Element", "Value" );
 
   for ( i = 0; i < 10; i++ ) {
      printf( "%7d%13d\n", i, n[ i ] );
   }
 
   return 0;  
} 
