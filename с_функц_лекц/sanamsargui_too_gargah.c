#include <stdio.h>
#include <stdlib.h>
 
int main( void )
{
   int i; /* тоолуур хувьсагч */
   
   /* 20 удаа давтана */
   for ( i = 1; i <= 20; i++ ) {
  
      /* 1-ээс 6 хүртэлх санамсаргүй тоог гаргах */
      printf( "%10d", 1 + ( rand() % 6 ) );
 
      /* Хэрэв тоолуур 5-д хуваагдвал шинэ мөрийг эхлүүлнэ*/
      if ( i % 5 == 0 ) {
         printf( "\n" );
      } /* if төгсгөл */
 
   } /* for төгсгөл */
 
   return 0; 
 
} /* main функц төгсгөл */
